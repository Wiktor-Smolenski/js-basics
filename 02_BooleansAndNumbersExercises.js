//true = 1, false = 0
//prefix postfix
let a = 5;
console.log(++a); //6
a = 5;
console.log(a); //5
console.log(a++); //5
console.log(a); //6

//both of these do the same thing
let num = 5;
num = num + 2;

console.log(num); // 7

num = 5;
num += 2;

console.log(num); // 7

